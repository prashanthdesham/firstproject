public class SwitchCase {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		int age;
		age=2;
		switch(age) {
		case 1:
			System.out.println("prashanth");
			break;
		case 2:
			System.out.println("rajesh");
			break;
		case 3:
			System.out.println("dinesh");
			break;
			default:
				System.out.println("no one match with is age");
				break;
				
		}
	}

}
